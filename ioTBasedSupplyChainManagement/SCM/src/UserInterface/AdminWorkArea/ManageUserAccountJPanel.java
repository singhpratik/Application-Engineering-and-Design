    public void popOrganisationComboBox() {
        organisationJComboBox.removeAllItems();

        for (Organisation organisation : enterprise.getOrganisationDirectory().getOrganisationList()) {
            organisationJComboBox.addItem(organisation);
        }
        
        int i = organisationJComboBox.getItemCount();
        if(i==0)
        {
        nameJTextField.setEnabled(false);
        passwordJTextField.setEnabled(false);
        employeeJComboBox.setEnabled(false);
        roleJComboBox.setEnabled(false);
        jTextField1.setEnabled(true);
        jTextField1.setText("Enter the Organisation and the Employee");
        jTextField1.setBackground(Color.RED);
        createUserJButton.setEnabled(false);
        }
        
        int j = employeeJComboBox.getItemCount();
                
        if(j==0){
          nameJTextField.setEnabled(false);
        passwordJTextField.setEnabled(false);
        employeeJComboBox.setEnabled(false);
        roleJComboBox.setEnabled(false);
        jTextField1.setEnabled(true);
        jTextField1.setText("Enter the Organisation and the Employee");
        jTextField1.setBackground(Color.RED);
        createUserJButton.setEnabled(false);
    }
    }
    
    public void populateEmployeeComboBox(Organisation organisation){
        employeeJComboBox.removeAllItems();
        
        for (Employee employee : organisation.getEmployeeDirectory().getEmployeeList()){
            employeeJComboBox.addItem(employee);
        }
    }
    
    private void populateRoleComboBox(Organisation organisation){
        roleJComboBox.removeAllItems();
        for (Roles role : organisation.getSupportedRole()){     
            roleJComboBox.addItem(role);
        }
    }

    public void popData() {

        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();

        model.setRowCount(0);

        for (Organisation organisation : enterprise.getOrganisationDirectory().getOrganisationList()) {
            for (UserAccount ua : organisation.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[2];
                row[0] = ua;
                row[1] = ua.getRole();
                ((DefaultTableModel) userJTable.getModel()).addRow(row);
            }
        }
    }

