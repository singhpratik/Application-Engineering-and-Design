    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
try{
        String userName = nameJTextField.getText();
        String password = passwordJTextField.getText();
        
        ValidatorClass validator = new ValidatorClass();
        
        boolean isNameEmoty = validator.isEmpty(userName);
        boolean isPassword = validator.isEmpty(password);
        
        if(isNameEmoty||isPassword)
        {
            JOptionPane.showMessageDialog(null, "Mandatory fields cannot be empty","ERROR", JOptionPane.ERROR_MESSAGE);
       
        }else{
        Organisation organisation = (Organisation) organisationJComboBox.getSelectedItem();
        Employee employee = (Employee) employeeJComboBox.getSelectedItem();
        Roles role = (Roles) roleJComboBox.getSelectedItem();
        
        for(UserAccount username:enterprise.getUserAccountDirectory().getUserAccountList())
        {
            if(username.toString().equals(userName))
            {
                 JOptionPane.showMessageDialog(null, "Username already exists.Please select another username","ERROR", JOptionPane.ERROR_MESSAGE);
       
            return;
            }
        }
           for(UserAccount username:organisation.getUserAccountDirectory().getUserAccountList())
           {
                 if(username.toString().equals(userName))
            {
                 JOptionPane.showMessageDialog(null, "Username already exists.Please select another username","ERROR", JOptionPane.ERROR_MESSAGE);
       
            return;
            }
           }
        
        organisation.getUserAccountDirectory().createUserAccount(userName, password, employee, role);
        callResset();
        JOptionPane.showMessageDialog(null, "Useraccount created successfully","INFO", JOptionPane.INFORMATION_MESSAGE);
       
        popData();
        }
}catch(Exception e)
{
    System.out.println("Error");
}
    }//GEN-LAST:event_createUserJButtonActionPerformed

    
