    public void populateRequestTable() {

        DefaultTableModel model = (DefaultTableModel) wareHouseRequestTable.getModel();
        model.setRowCount(0);

        for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[6];
            row[0] = request;
            row[1] = request.getDeliveryAddress();
            row[2] = request.getProduct();
            row[3] = request.getReceiver();
            row[4] = request.getStatus();
            String result = ((WarehouseRequest) request).getTestResult();
            row[5] = request.getShipmentNumber();

            model.addRow(row);

        }
        
        if(model.getRowCount()==0)
        {
            jButton7.setEnabled(false);
            jTextField1.setEnabled(false);
            jButton2.setEnabled(false);
        }else{
             jButton7.setEnabled(true);
            jTextField1.setEnabled(true);
            jButton2.setEnabled(true);
        }

    }

