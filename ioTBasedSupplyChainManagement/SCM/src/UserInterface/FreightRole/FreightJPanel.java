    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        int selVal = wareHouseRequestTable.getSelectedRow();
        if (selVal < 0) {
            JOptionPane.showMessageDialog(null, "Please select the request", "Request must be selected", JOptionPane.ERROR_MESSAGE);

        } else {
            WorkRequest request = (WorkRequest) wareHouseRequestTable.getValueAt(selVal, 0);
            request.setStatus("sent");
            request.setMessage("Dispatched");
            request.setStatus("Sent");
            populateRequestTable();
            JOptionPane.showMessageDialog(null, "Trailer Rerouted Successfully", "Info", JOptionPane.INFORMATION_MESSAGE);

        }

    }//GEN-LAST:event_jButton6ActionPerformed

